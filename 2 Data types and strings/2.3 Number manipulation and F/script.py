weight = 70  # weight in kilograms
height = 1.75  # height in meters

bmi =  weight / (height ** 2)

print(f"Your BMI is: {bmi}")  # Output the BMI value
print(round(bmi))  # Output the rounded BMI value
print(round(bmi, 2))  # Output the BMI value rounded to 2 decimal places
print(type(bmi))  # Output the data type of bmi


